Setelah puas menikmati keindahan dinding dan air terjun Lembah Harau, perjalanan selanjutnya diteruskan menuju Jembatan Kelok 9 untuk menyaksikan keindahan panorama alam yang dari dasar lembahnya sampai ke puncak lembah terhubung oleh sebuah jembatan layang yang didampingi oleh indah nya liku Jalan Kelok 9 sendiri.<br>
Kemudian tour mengarah ke Batusangkar dimana rombongan akan dibawa mengunjungi Istana Basa Pagaruyung yang merupakan sebuah komplek rumah Gadang kediaman raja Minangkabau dahulunya di Kab. Tanah Datar. Setelah itu Istirahat dan makan siang di Nagari Sungai Tarab sambil menikmati hamparan sawah nan hijau dan berjinjang.<br>
Selanjutnya tour menuju Danau Singkarak yang merupakan danau terbesar di Sumatera Barat dan sangat terkenal akan Ikan Bilisnya. Setelah cukup beristirahat ditepian danau yang asri perjalanan diteruskan menuju kota Bukittinggi untuk check in hotel di Bukittinggi. Nantinya rombongan akan dibawa keluar untuk makan malam di restaurant kesukaan anda.<p>

Day 3<br>
Selesai sarapan pagi perjalanan dimulai menuju Danau Maninjau yang merupakan salah satu danau terindah yang ada di Sumatera. Setelah melalui perjalanan perbukitan yang menghijau, kita akan coffee break di Nagari Sungai Landia untuk melepas penat sambil menikmati panorama alamnya nan indah menawan. Setelah itu dilanjutkan menuju Puncak Lawang dimana mata kita akan dimanjakan oleh keelokan alam Danau Maninjau yang merupakan sebuah caldera yang terindah di Sumbar. Kemudian perjalanan diteruskan menuju Nagari Lawang untuk melihat proses pembuatan gula merah tebu dan kacang goreng yang dikelola secara traditional oleh warga sekitar kampung tersebut. Setelah itu diteruskan menuju Bukittinggi untuk istirahat makan siang. Pada sore harinya kita mengunjungi Taman Jam Gadang yang merupakan icon wisata Bukittinggi dan bersantai ria di kawasan Pasar Atas Bukittinggi. Kembali ke hotel dan makan malam.<p>








Day 4<br>
Setelah selesai sarapan pagi langsung check out hotel dan melanjutkan perjalanan ke Padang ibukota Provinsi Sumatera Barat. Selain terkenal akan Pantai dan Wisata baharinya, Padang dikenal juga sebagai pusat pendidikan Universitas Andalasnya.<br>
