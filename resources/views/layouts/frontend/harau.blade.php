	Rangkaian Kegiatan :<p>
	Day 1<br>
Rombongan akan dijemput oleh team kami di Padang Airport Sumbar dan langsung memulai perjalanan menuju kawasan hutan lindung Lembah Anai yang berada dipinggir jalan utama Padang – Bukittinggi. Istirahat sejenak untuk menikmati keindahan dan sambil  merasakan kesejukan Air terjun Lembah Anai yang bersumber dari mata air pegunungan Singgalang.
Setelah itu tour dilanjutkan menuju Pusat Dokumentasi dan Informasi Kebudayaan Minangkabau yang juga merupakan komplek miniatur kampung Minang dengan beragam Rumah Gadang yang berada di Padang Panjang dan dilanjutkan dengan istirahat makan siang. Perjalanan selanjutnya diteruskan menuju menuju kampung yang terkenal akan  kerajinan tangan tenun Songket dan ukiran kayu yang berada di Nagari Pandai Sikek.
Sore harinya perjalanan diteruskan menuju kota Payakumbuh untuk menuju Lembah Harau. Makan malam dan check in hotel di Lembah Harau.<p>
	
Day 2<br>
